package com.javaWrapperClasses;

public class Example1 {

	public static void main(String[] args) {
		
		
		//primitive byte value
		byte byteValue = 20;
		
		System.out.println(byteValue);
		
		System.out.println("------------------------------------");
		
		System.out.println(Byte.BYTES);//1 byte
		System.out.println(Byte.SIZE);//1 byte = 8 bits

		System.out.println("------------------------------------");
		
		System.out.println(Byte.MAX_VALUE);//max value of byte data type
		System.out.println(Byte.MIN_VALUE);//min value of byte data type
		
	}

}
